function renderizarGaleria() {
    let index = 0; // Contador para crear IDs de salto fáciles
    
    for (const [titulo, imagenes] of Object.entries(galeriaPorCategorias)) {
        // 1. Crear la sección de la categoría
        const seccion = document.createElement('section');
        seccion.className = 'categoria';
        seccion.id = `cat-${index}`; // Esto genera: id="cat-0", id="cat-1", etc.
        index++;

        // 2. Crear y agregar el título de la categoría
        const header = document.createElement('header');
        header.className = 'categoria-header';
        header.innerHTML = `<h2>${titulo}</h2>`;
        seccion.appendChild(header);

        // 3. Crear el contenedor de las tarjetas (grid)
        const grid = document.createElement('div');
        grid.className = 'categoria-grid';

        // 4. Crear y agregar las tarjetas de imagen
        imagenes.forEach(imagen => {
            const card = document.createElement('article');
            card.className = 'card';
            card.innerHTML = `
                <img src="${imagen.src}" alt="${imagen.descripcion}" loading="lazy">
                <div class="card-description">${imagen.descripcion}</div>
            `;
            grid.appendChild(card);
        });

        // 5. Agregar el grid a la sección y la sección al main
        seccion.appendChild(grid);
        mainElement.appendChild(seccion);
    }
}
// Ejecutar la función al cargar la página
renderizarGaleria();